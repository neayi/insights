Bonjour,<br/><br/>

Et merci d'avoir décidé de rejoindre la plateforme TriplePerformance.<br/><br/>

Cette plateforme vous permet de rester en contact avec vos agriculteurs.
Pour cela vous pouvez paramétrer des règles de notification afin de recevoir un mail quand un de
ceux-ci pose une question par exemple. Vous pouvez aussi recevoir des emails pour des questions posées sur certains sujets en ajoutant les mots clés en favoris.<br/><br/>

Quand un agriculteur pose une question, sentez-vous libre d'y répondre. Si vous connaissez la réponse ou pensez pouvoir la trouver rapidement,
il sera content d'avoir votre réponse, mais sinon l'ensemble de la communauté est là pour l'aider dans sa question.<br/><br/>

Quand vous répondez, pensez que d'autres liront votre réponse, y compris dans le futur, et dans d'autres contextes.
Ne donnez pas d'informations confidentielles, soyez précis dans votre réponse, en indiquant des sources complémentaires
(lien vers la base de connaissances ou vers un site web). Attention à ne pas mettre de lien vers une page qui nécessiterait une connexion (par exemple votre intranet).<br/><br/>

Si vous souhaitez indiquer à l'agriculteur des indications complémentaires, comme le nom d'une personne qu'il peut contacter,
ou d'une page qui n'est accessible que via votre intranet, vous pouvez lui envoyer un message privé en cliquant sur son profil.<br/><br/>

Vous trouverez plus d'information sur la manière d'utiliser la plateforme ici :
